        {{-- Section 1 --}}
        <h2 id="why-ai-translation">Why AI PDF Translation Is Better Than Copy-Paste</h2>
        <p>
            The obvious workaround is to copy text out of a PDF, paste it into Google Translate, and piece the result
            together. In practice, this approach breaks down quickly for several reasons:
        </p>
        <ul>
            <li>
                <strong>PDF text extraction is messy.</strong> Most PDFs don't have clean copy-paste behaviour. Column
                layouts, headers, footers, footnotes, and hyphenated words all get jumbled when you select and copy.
                What you paste into Google Translate may be out of order or missing entire sections.
            </li>
            <li>
                <strong>Google Translate has a 5,000-character limit per request.</strong> A standard two-page A4
                document in English contains roughly 4,000–6,000 characters. Anything longer requires multiple rounds
                of copy-pasting, manual stitching, and careful tracking of which sections you've already translated.
            </li>
            <li>
                <strong>Context is lost across chunks.</strong> When you split a document into chunks, the translation
                engine loses the sentence context that spans the chunk boundary. This leads to awkward breaks and
                mistranslations at those seams.
            </li>
            <li>
                <strong>Formatting disappears entirely.</strong> Paragraph breaks, bullet points, numbered lists, bold
                headings — all of it collapses into a flat wall of text. Reconstructing the structure manually defeats
                the purpose of a quick translation.
            </li>
        </ul>
        <p>
            AI-powered PDF translation processes the entire document as a single unit. The model understands paragraph
            boundaries, maintains the logical flow of numbered lists and headers, and preserves the document structure
            in the translated output. For Bengali specifically — a language with a distinct script (বাংলা লিপি) and
            complex sentence structure — having full document context produces dramatically more natural results.
        </p>

