        {{-- Section 5 --}}
        <h2 id="tips">Tips for Better Translation Results</h2>
        <p>
            AI translation is powerful, but you can improve the quality of your output significantly with a few
            simple practices:
        </p>
        <ul>
            <li>
                <strong>Use text-based PDFs, not scanned images.</strong> If your PDF was scanned (i.e., you can't
                select or copy its text), the translation tool can't extract the content directly. You'll need to
                run OCR first to make the text selectable before translating.
            </li>
            <li>
                <strong>Shorter documents translate faster and more accurately.</strong> For very long documents
                (over 20 pages), consider splitting the PDF first and translating section by section. You'll get
                better results and can review each section independently.
            </li>
            <li>
                <strong>Check proper nouns manually.</strong> Names of people, organisations, places, and brand
                names may be transliterated differently by the AI than your audience expects. Verify these
                specifically, especially in legal or medical documents where precision matters.
            </li>
            <li>
                <strong>Use a Bengali-script keyboard to verify key passages.</strong> If you're uncertain about
                a translation, use a Bengali virtual keyboard (available at google.com/intl/bn/inputtools) to
                type the equivalent term and compare. This is especially useful for technical or medical terminology.
            </li>
            <li>
                <strong>Don't translate PDFs with lots of tables or columns without checking.</strong> Complex
                tabular data (financial statements, comparison sheets) may lose their column alignment in
                translation. Verify numerical data against the original after downloading.
            </li>
        </ul>

