<h2 id="tips">Tips for Academic and Legal Documents</h2>

<h3>Academic Submissions (APA, MLA, Chicago)</h3>
<p>
    <strong>APA format:</strong> Page numbers go in the top right corner. The running head (abbreviated title) goes in the top left. Start numbering from page 1 (the title page). Use Arabic numerals.
</p>
<p>
    <strong>MLA format:</strong> Your last name and the page number go in the top right corner (e.g., "Smith 3"). PDFTash's page number tool adds numbers; you can add the name prefix in the text field.
</p>
<p>
    <strong>Theses and dissertations:</strong> Typically use Roman numerals for the front matter (abstract, table of contents, acknowledgements) and switch to Arabic numerals for the body chapters. Process the front matter and body sections separately with their respective numbering styles, then use the <a href="/merge-pdf">PDFTash Merge tool</a> to combine them.
</p>

<h3>Legal Documents</h3>
<p>
    Court filing requirements vary by jurisdiction, but most require sequential page numbers, often in the bottom centre or bottom right. Check your specific court's filing rules. For documents that span multiple filings (exhibits, annexes), confirm whether each exhibit should restart at page 1 or continue from the main document's numbering.
</p>

