<h2 id="why-page-numbers">Why Page Numbers Matter</h2>
<p>
    Page numbers are functional, not decorative. Here are the most common real-world reasons to add them:
</p>
<ul>
    <li><strong>Contracts and legal documents:</strong> Both parties need to reference specific clauses by page number during review and negotiation. Courts and regulatory bodies often require sequentially numbered pages in filed documents.</li>
    <li><strong>Academic submissions:</strong> University style guides (APA, MLA, Chicago, Harvard) all require page numbers in specific positions. Submissions without page numbers are typically returned for correction.</li>
    <li><strong>Business reports:</strong> Stakeholders, board members, and clients refer to specific pages during presentations and meetings. "Page 7" is far more useful than "about two-thirds through."</li>
    <li><strong>Multi-part documents:</strong> When a larger document is split and merged from multiple sources, page numbers ensure the assembled PDF reads as a coherent whole.</li>
    <li><strong>Printed manuals and instruction guides:</strong> Physical printed copies become useless without page numbers if pages get separated or reordered.</li>
</ul>

<h2 id="position-options">Position and Format Options</h2>
<p>PDFTash gives you full control over how page numbers appear:</p>

<h3>Position</h3>
<ul>
    <li><strong>Bottom centre</strong> — the most common default, works for most documents</li>
    <li><strong>Bottom right</strong> — standard for academic papers (APA, MLA)</li>
    <li><strong>Bottom left</strong> — less common, used in some legal formats</li>
    <li><strong>Top centre</strong> — used in some report and manual styles</li>
    <li><strong>Top right</strong> — common in legal briefs and official submissions</li>
</ul>

<h3>Starting Number</h3>
<p>
    You can set the page number to start from any value. This is essential for multi-part documents — if Part 1 ends on page 47, Part 2 should start from 48, not 1. PDFTash's starting number field accepts any positive integer.
</p>

<h3>Style</h3>
<ul>
    <li><strong>Arabic numerals:</strong> 1, 2, 3 — universal default</li>
    <li><strong>Roman numerals:</strong> i, ii, iii — traditional for prefatory pages (table of contents, introduction) in books and academic theses</li>
</ul>

