<h2 id="what-is-esig">What Is an Electronic Signature?</h2>
<p>
    An <strong>electronic signature</strong> (e-signature) is any digital mark applied to a document to show intent to agree. It doesn't have to look like your handwritten signature — it can be a typed name, a drawn scrawl, or a scanned image of your ink signature. What matters legally is that it was applied with intent to sign.
</p>
<p>
    This is different from a <strong>digital signature</strong>, which is a cryptographic certificate issued by a Certificate Authority (CA). Digital signatures are used in high-security government and financial workflows and require identity verification. For everyday documents — leases, NDAs, freelance contracts, school forms — a standard electronic signature is perfectly sufficient and legally binding in the EU (eIDAS regulation), the US (ESIGN Act and UETA), and most other jurisdictions worldwide.
</p>

<div class="callout" style="border-left-color:#7c5cfc;">
    <p><strong>Quick distinction:</strong> Electronic signature = your mark on a document (free, instant). Digital signature = cryptographic identity certificate (requires verification, often paid). Most people need the first kind.</p>
</div>

