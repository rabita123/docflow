        <h2 id="file-order">How to Control Page Order When Merging</h2>
        <p>
            The most common complaint after merging PDFs is that the pages came out in the wrong order. This almost
            always happens for one of two reasons: the files were uploaded in the wrong sequence, or the drag-to-reorder
            list wasn't double-checked before merging.
        </p>
        <p>
            PDFTash shows each uploaded file as a labelled card in the merge queue. The <strong>order of the cards
            top-to-bottom is the order of pages in your final document</strong>. File 1 at the top becomes pages 1 to N
            of the merged PDF; File 2 becomes the next block of pages, and so on. To change the order, simply drag a
            card to its correct position before you hit Merge.
        </p>
        <p>
            Here are the mistakes to watch out for:
        </p>
        <ul>
            <li>
                <strong>Uploading files in the wrong order and not reordering.</strong> If you upload your portfolio
                first and your CV second, that's the order they'll appear in the merged file — portfolio first, CV
                second. Always check the card order before merging.
            </li>
            <li>
                <strong>Having the cover page as the last file in the queue.</strong> A cover sheet should always be
                dragged to the top of the list so it becomes page 1 of the merged PDF.
            </li>
            <li>
                <strong>Confusing file sort order with upload order.</strong> When you select multiple files in a
                file-browser dialog, your operating system may add them in alphabetical or date-modified order, not
                necessarily the order you intended.
            </li>
        </ul>
        <p>
            <strong>Practical tip:</strong> If you regularly merge the same set of files (e.g. monthly reports with a
            fixed structure), rename your files with a numeric prefix before uploading — for example
            <code>01_cover.pdf</code>, <code>02_report.pdf</code>, <code>03_appendix.pdf</code>. Most operating
            systems will then sort them in the correct order automatically when you multi-select, saving you the drag
            step.
        </p>

