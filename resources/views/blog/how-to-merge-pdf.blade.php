        <div style="margin-bottom: 28px;">
            <h3>Will my merged PDF be searchable?</h3>
            <p>
                If the source PDFs contain searchable text (i.e. they were created from Word, Google Docs, or similar,
                or have had OCR applied), the merged result will also be fully searchable. The merging process does not
                remove or alter any text layer. If you merge scanned PDFs that have no text layer, the result will be a
                non-searchable image PDF — the same as the originals.
            </p>
        </div>

        <div style="margin-bottom: 28px;">
            <h3>Can I merge password-protected PDFs?</h3>
            <p>
                PDFTash can merge password-protected PDFs if you enter the password when prompted during the upload
                step. Files with an "owner password" (which restricts editing but not opening) are handled
                automatically. Files with a "user password" (which prevents the file from even being opened without
                the password) require you to provide the password. PDFTash cannot bypass passwords on locked PDFs —
                this is an intentional security boundary.
            </p>
        </div>

        <div style="margin-bottom: 28px;">
            <h3>Does merging reduce the quality of my PDFs?</h3>
            <p>
                No. Merging PDFs is a non-destructive operation — PDFTash combines the pages from your source files
                without re-encoding or compressing any content. Images, fonts, and vector graphics remain exactly as
                they were in the originals. The only change is that the pages are now inside a single file container.
                If you want to reduce the file size <em>after</em> merging, you can run the merged PDF through the
                compressor as a separate step.
            </p>
