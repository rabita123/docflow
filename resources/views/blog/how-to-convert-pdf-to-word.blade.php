            would serve you better.
        </p>

        {{-- Section 6 --}}
        <h2 id="scanned-pdfs">A Note on Scanned PDFs</h2>
        <p>
            Scanned PDFs deserve a special mention because they behave completely differently from digital PDFs. A
            scanned PDF is essentially a <strong>photograph of a paper document</strong> embedded inside a PDF
            container. There's no text layer — just pixels. Trying to convert it to Word without OCR produces a Word
            document full of images, not editable text.
        </p>
        <p>
            PDFTash automatically detects scanned PDFs and applies OCR before converting to Word. The accuracy of
            OCR depends on:
        </p>
        <ul>
            <li><strong>Scan resolution:</strong> 300 DPI or higher gives excellent results. Below 200 DPI, accuracy
            drops noticeably.</li>
            <li><strong>Font clarity:</strong> Clean, printed fonts convert well. Handwriting is much harder.</li>
            <li><strong>Page orientation:</strong> Skewed or rotated pages reduce accuracy — most scanners have
            auto-correction, or you can use <a href="/ocr-pdf">PDFTash's OCR tool</a> to pre-process.</li>
            <li><strong>Background noise:</strong> Yellowed paper, ink bleed, and coffee stains all reduce OCR
            accuracy. There's no fix for a very low-quality scan.</li>
        </ul>

        <div class="callout green">
            <p>
                <strong>Pro tip:</strong> If you have access to the original paper document, scan it at 300 DPI in
                greyscale (not colour) for the best OCR results. Greyscale scans produce sharper text edges than
                colour scans because they avoid JPEG colour artefacts around letterforms.
            </p>
        </div>

        <p>
            After OCR-based conversion to Word, expect to do more cleanup than with a digital PDF. Headers and
            footers may appear as body text, paragraph breaks may be inconsistent, and special characters (em dashes,
            curly quotes, non-breaking spaces) sometimes convert to question marks or generic symbols. That said,
            even an imperfect OCR conversion saves enormous time compared to retyping a document from scratch.
        </p>

        <p>
            If you specifically need a clean, searchable text layer added to a scanned PDF without converting to
            Word, check out <a href="/ocr-pdf">PDFTash's dedicated OCR tool</a> — it adds a text layer to the PDF
            itself, keeping the original appearance intact.
        </p>

