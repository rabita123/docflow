        {{-- Section 1 --}}
        <h2 id="why-pdfs-arent-editable">Why PDFs Aren't Editable (and Why That Matters)</h2>
        <p>
            PDF stands for Portable Document Format, and "portable" is the operative word. Adobe designed the format
            in the early 1990s with one goal: a document that looks <em>exactly</em> the same on every screen, printer,
            and operating system, regardless of which fonts or software the viewer has installed.
        </p>
        <p>
            To achieve that, PDFs store content as a fixed layout — each character is placed at precise x/y coordinates
            on a virtual page, with rendering instructions baked in. There's no concept of "paragraphs" or "columns"
            the way Word understands them. Instead, the PDF viewer just draws pixels at the right positions. This is
            why you can't click into a PDF paragraph and start typing: there are no paragraphs in the traditional sense,
            just positioned text objects.
        </p>
        <p>
            Converting PDF to Word means reconstructing those text objects into actual paragraphs, detecting where
            headings begin and end, identifying table grids from visual spacing, and rebuilding columns from the
            x-coordinate layout. It's a genuinely complex process — but modern converters handle it well for most
            standard business documents.
        </p>

        <div class="callout" style="border-left-color: #5b5cff;">
            <p>
                <strong>Key insight:</strong> Converting a PDF created from a Word file (a "digital PDF") almost always
                produces a near-perfect result. Converting a scanned PDF (essentially a photograph of a document) requires
                OCR and produces good-but-not-perfect results depending on scan quality.
            </p>
        </div>

