        {{-- Section 2: Free Limits --}}
        <h2 id="free-limits">Free Tier Limits: What You Actually Get</h2>
        <p>
            This is where the biggest differences lie. All three tools advertise a free plan, but the reality of what
            you can do without a credit card varies considerably.
        </p>

        <h3>iLovePDF Free</h3>
        <p>
            iLovePDF allows <strong>2 tasks per day</strong> on the free plan, and no account is required for those
            tasks. "Tasks" covers their core operations: compress, merge, split, convert, and a handful of others. The
            50 MB file size limit is generous — most everyday PDFs fall comfortably under this threshold. The main
            friction is the 2-task daily cap: if you need to compress a file <em>and</em> then merge it in the same
            session, you've used your daily allowance.
        </p>

        <h3>SmallPDF Free</h3>
        <p>
            SmallPDF's free plan is the most restrictive of the three. You get <strong>2 tasks per day</strong>, but
            more significantly, SmallPDF requires you to create an account after completing your first free task. This
            means that even for a single one-off compression job, SmallPDF will prompt you to sign up with an email
            address. The 5 MB file size limit compounds the frustration — a single scanned page can easily be 3–5 MB,
            meaning many real-world documents are simply rejected before you even start.
        </p>

        <h3>PDFTash Free</h3>
        <p>
            PDFTash takes a different approach: <strong>all basic tools are unlimited on the free plan</strong> — no
            daily cap, no signup, no watermark. Compress, merge, split, sign, and edit as many documents as you need.
            The only limit is the file size (10 MB per file on the free tier). AI-powered tools (translate, chat with
            PDF, summarize, generate) are limited to <strong>1 task per day</strong> on the free plan, with unlimited
            usage on Pro.
        </p>

