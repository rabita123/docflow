        {{-- Section 6: Privacy --}}
        <h2 id="privacy">Privacy and Data Handling</h2>
        <p>
            When you upload a PDF to any online tool, your document leaves your device and is processed on a remote
            server. Understanding what happens to it matters — especially for confidential documents like contracts,
            medical records, or financial statements.
        </p>
        <p>
            All three tools claim to delete uploaded files after processing. Here's what each says:
        </p>
        <ul>
            <li>
                <strong>iLovePDF:</strong> Files are deleted from servers within <strong>2 hours</strong> of upload.
                No account needed for basic tasks, so no personally identifiable information is collected if you use
                it without logging in.
            </li>
            <li>
                <strong>SmallPDF:</strong> Files are deleted within <strong>1 hour</strong> — the fastest of the
                three. However, SmallPDF requires account registration after your first task, which means your email
                address is stored even for free users. Usage data is tied to your account.
            </li>
            <li>
                <strong>PDFTash:</strong> Files are deleted within <strong>2 hours</strong>. Crucially, the no-signup
                model means that for basic tasks, <strong>zero personally identifiable information is collected</strong>
                — there is no account, no email, nothing linking the uploaded file to you.
            </li>
        </ul>
        <p>
            For maximum privacy with a sensitive document, PDFTash's no-signup approach is the clearest option: there
            is no account for the file upload to be associated with. That said, for any highly sensitive document —
            attorney-client privileged material, medical records, trade secrets — review the platform's privacy policy
            and consider whether an offline solution is more appropriate.
        </p>

