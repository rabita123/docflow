<h2 id="what-is-redaction">What Is PDF Redaction vs. Deleting Text?</h2>
<p>
    Redaction means <strong>permanently removing</strong> information from a document so it cannot be recovered. When done correctly, redacted text is replaced with a solid black box, and the underlying data is completely erased from the PDF's content stream — not just hidden from view.
</p>
<p>
    This is critically different from simply drawing a black rectangle over text in a PDF editor, or changing the font colour to black. Both of those methods <em>hide</em> the text visually but leave the original data intact inside the PDF file. Anyone who opens the file in a text editor, copies all text, or uses "find" can still read the supposedly hidden information. High-profile data breaches have occurred exactly this way — including a famous incident where a US government legal brief had names "blacked out" with a text box that could be copy-pasted away in seconds.
</p>
<p>
    True redaction, as performed by PDFTash, physically removes the data from the PDF and renders it as a genuine black rectangle that contains no underlying information.
</p>

<h2 id="why-it-matters">Why Proper Redaction Matters</h2>
<p>
    Professional and legal contexts where redaction is essential include:
</p>
<ul>
    <li><strong>Court and legal documents:</strong> Filed documents often require redaction of personal identifiers (names of minors, Social Security numbers, financial account numbers) under court rules.</li>
    <li><strong>Medical and healthcare:</strong> HIPAA (US) and GDPR (EU) require that patient health information is protected when sharing documents with third parties who don't need the full details.</li>
    <li><strong>Human resources:</strong> Salary information, disciplinary records, and personal details of employees must be protected when sharing HR documents with auditors, lawyers, or other departments.</li>
    <li><strong>Government and FOIA requests:</strong> Agencies responding to freedom of information requests routinely redact exempt information before releasing documents.</li>
    <li><strong>Financial services:</strong> Account numbers, card numbers, and financial statements shared with third parties require redaction of any identifying information that isn't relevant to the recipient.</li>
</ul>

<h2 id="what-to-redact">What Information Should You Redact?</h2>
<p>
    Common categories of personally identifiable information (PII) and sensitive data to redact:
</p>
<ul>
    <li>Full names (when combined with other identifiers)</li>
    <li>Social Security numbers, national ID numbers, passport numbers</li>
    <li>Email addresses and phone numbers</li>
    <li>Home addresses and location data</li>
    <li>Bank account and credit card numbers</li>
    <li>Medical record numbers, diagnoses, and prescription details</li>
    <li>Salaries, financial details, and trade secrets</li>
    <li>Signatures and dates of birth</li>
    <li>IP addresses and device identifiers in technical documents</li>
</ul>

<div class="callout" style="border-left-color:#7c5cfc;">
    <p><strong>GDPR tip:</strong> Under European data protection law, even a name combined with a job title can constitute personal data. When in doubt about what to redact for compliance purposes, consult your data protection officer.</p>
</div>

