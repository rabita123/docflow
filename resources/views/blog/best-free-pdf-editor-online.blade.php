<h3>SmallPDF</h3>
<p>
    SmallPDF has an excellent interface and a wide tool set, but the free tier adds a visible watermark to compressed and converted PDFs and limits you to two operations per day. It's good for occasional one-off use but impractical for anyone with regular PDF work. The Pro plan at $12/month removes these restrictions.
</p>

<h3>ILovePDF</h3>
<p>
    ILovePDF covers most standard PDF operations (merge, split, compress, convert) and doesn't require signup for basic tasks. File size limits on the free tier are lower than PDFTash, and there are no AI features. A solid choice for simple operations.
</p>

<h3>Sejda</h3>
<p>
    Sejda's PDF editor is notably good for adding text directly to PDFs — you can click anywhere on the page and type, which is useful for filling in forms that don't have interactive fields. The free tier is limited to 3 tasks per day and 50 pages per document. No AI features.
</p>

<h3>PDF24</h3>
<p>
    PDF24 is genuinely unlimited on the free tier and doesn't add watermarks — but the interface is dated, mobile support is patchy, and there are no AI-powered tools. Good for basic bulk operations where you need to process many files.
</p>

