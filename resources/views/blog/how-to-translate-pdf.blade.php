<h2 id="why-hard">Why PDF Translation Is Hard</h2>
<p>
    PDFs are layout documents, not structured documents like Word files. Text in a PDF isn't stored as flowing paragraphs — it's stored as individual text objects placed at specific coordinates on the page. A single sentence might be broken into five separate text objects in the PDF's internal structure.
</p>
<p>
    When you copy-paste from a PDF, the PDF reader reassembles these objects in reading order (roughly left-to-right, top-to-bottom) and gives you the text. But the structure — which text is a heading, which is a table cell, which is a bullet point — is completely lost. Translating that unstructured text and pasting it back produces unusable output.
</p>
<p>
    True PDF translation requires understanding the document's visual structure, translating each element in context, and reconstructing the layout with the translated content — which is why most tools get this wrong, and why an AI-powered approach is necessary.
</p>

