<h2 id="accuracy-tips">Accuracy Tips</h2>
<ul>
    <li><strong>Text PDFs translate better than scanned PDFs.</strong> Text PDFs have clean character data; scanned PDFs require OCR first, and any OCR errors will appear in the translation. For critical documents, check the OCR output before translating.</li>
    <li><strong>Technical documents need review.</strong> Industry-specific terminology (medical, legal, financial) may be translated literally rather than idiomatically. Always have a domain expert review professional translations before sharing officially.</li>
    <li><strong>Very long documents (50+ pages) take longer.</strong> Large documents process in segments. For a 100-page report, expect 2–3 minutes of processing time.</li>
    <li><strong>Simple layouts translate most accurately.</strong> Single-column text is easier to reconstruct than complex multi-column magazine-style layouts.</li>
</ul>

