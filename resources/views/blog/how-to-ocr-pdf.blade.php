<h2 id="accuracy-tips">Accuracy Tips</h2>
<p>OCR accuracy depends heavily on scan quality. Here's how to get the best results:</p>
<ul>
    <li><strong>Scan at 300 DPI minimum.</strong> At lower resolutions, characters become too small for reliable recognition. 300–600 DPI is the sweet spot for text documents.</li>
    <li><strong>Use black and white or grayscale scanning for text documents.</strong> Colour scanning increases file size without improving text recognition accuracy.</li>
    <li><strong>Ensure the document is flat and straight.</strong> Curved or angled pages reduce accuracy. PDFTash applies deskewing correction automatically, but severe angles (more than 15°) can still cause issues.</li>
    <li><strong>Good lighting, no shadows.</strong> If photographing a document with a phone, ensure even lighting with no shadow falling across the text.</li>
    <li><strong>Select the correct language.</strong> The single biggest accuracy improvement for non-English documents is selecting the correct source language in PDFTash.</li>
</ul>

