<h2 id="what-is-ocr">What Is OCR and How Does It Work?</h2>
<p>
    Optical Character Recognition is a technology that converts images of text into machine-readable text. When you scan a document, you capture a photograph — the characters are shapes drawn in ink on paper, not digital text. OCR software analyses those shapes, compares them against known character patterns, and outputs the text equivalent.
</p>
<p>
    Modern OCR uses deep learning models trained on millions of document images in dozens of scripts and languages. PDFTash uses Tesseract OCR (the world's most widely deployed open-source OCR engine, originally developed by HP and now maintained by Google) combined with image pre-processing that dramatically improves accuracy on low-quality scans, rotated pages, and low-contrast documents.
</p>
<p>
    The output is a PDF with an invisible text layer behind the image. The page still looks like the original scan, but now the text is fully selectable, searchable, and copyable. This is called a "searchable PDF" or "PDF with OCR layer."
</p>

<h2 id="when-you-need">When You Need OCR</h2>
<ul>
    <li><strong>Scanned physical documents:</strong> Contracts, certificates, medical records, bank statements, and academic transcripts that were physically scanned.</li>
    <li><strong>PDFs generated by older software</strong> that saved pages as images rather than as text data (common in some government and legal systems).</li>
    <li><strong>Photographed documents:</strong> PDFs created from phone photos of documents.</li>
    <li><strong>Fax-converted PDFs:</strong> Documents received as fax often have no text layer.</li>
    <li><strong>Archives and research:</strong> Old books, newspapers, and historical documents digitised from microfilm or photography.</li>
</ul>
<p>
    <strong>How to tell if your PDF needs OCR:</strong> Try to click and drag to select text on the page. If you can highlight individual words, the PDF already has a text layer and doesn't need OCR. If clicking selects the entire page like a photo, or if nothing highlights, OCR is needed.
</p>

